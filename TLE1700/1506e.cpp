using namespace std;

int n, m, k, t = 1;
VI v, l, s;
PQ<int> y;

int solve(){
  cin >> n;
  k = 1;
  int prev = -1;

  v.clear();
  l.clear();
  s.clear();

  R(i, n){
    scanf("%d", &m);
    if(m != prev){
      v.PB(m);
      l.PB(0);
      prev = m;
      while(k < m){
        s.PB(k++);
      }
      k = m + 1;
    } else{
      l[len(l) - 1] ++;
    }
  }


  auto it = s.begin();

  R(i, len(v)){
    printf("%d ", v[i]);
    R(j, l[i]){
      printf("%d ", *it);
      ++it;
    }
  }

  cout << endl;

  k = 1;

  R(i, len(v)){
    printf("%d ", v[i]);
    while(k<v[i]){
      y.push(k++);
    }
    k = v[i] + 1;
    R(j, l[i]){
      printf("%d ", y.top());
      y.pop();
    }
  }

  cout << endl;
  return 0;
}

