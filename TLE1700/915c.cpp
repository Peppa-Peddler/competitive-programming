LL getper(LL num){
   stack<int> s;
   str = sec;
   string result = "";
   num = num - 1;
   for (int i = 1; i < str.size() + 1; i++) {
     s.push(num % i);
     num = num / i;
   }
   for (int i = 0; i < str.size(); i++) {
     int a = s.top();
     result += str[a];
     int j;
     for (j = a; j < str.length(); j++)
       str[j] = str[j + 1];
     str[j + 1] = '\0';
     s.pop();
   }
   return stoll(result);
}

