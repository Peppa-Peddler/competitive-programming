LL n, g, b, t = 1;

LL solve(){
  LL mid, gd, rem, ans;
  cin >> n >> g >> b;
  mid = n/2 + n%2;
  rem = n - mid;
  gd = mid/g + (mid%g!=0);
  ans = (gd - 1)*b + mid;
  rem = max(0LL, rem - (gd - 1)*b);
  return ans + rem;
}

