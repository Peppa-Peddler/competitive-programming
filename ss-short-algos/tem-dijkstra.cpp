using namespace std;

int n,m,t=1;

VI V[N], E[N];
int D[N];

void dijkstra(int source){
  int v,e, edge;
  clear(D, -1);
  PQ<PII> Q;
  Q.push(MP(0, source));
  while(!Q.empty()){
    v = Q.top().S;
    e = Q.top().F;
    Q.pop();
    if(D[v] != -1) continue;
    D[v] = -e;
    R(i, SZ(V[v])){
      edge = e - E[v][i];
      Q.push(MP(edge, V[v][i]));
    }
  }
}

int solve(){
  cin >> n >> m;
  int a,b,d;
  R(i, m){
    cin >> a >> b >> d;
    V[a].PB(b);
    V[b].PB(a);
    E[a].PB(d);
    E[b].PB(d);
  }

  dijkstra(0);

  R(i, n){
    cout << D[i] << " ";
  } cout << endl;

  return 0;
}

int main(){

  //cin >> t;
  while(t--) cout << solve() << endl;

}
