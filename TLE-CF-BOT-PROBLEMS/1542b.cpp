using namespace std;

LL n,t=1,a,b;

string solve(){
  cin >> n >> a >> b;

  if( n == 1 ) return "YES";

  LL exp = 1;

  if( a == 1 ) return (n-1) % b ? "NO":"YES";

  while( exp <= n ){
    if( (n - exp) % b == 0 ) return "YES";
    exp *= a;
  }

  return "NO";
}

int main(){

  cin >> t;
  while(t--) cout << solve() << endl;

}
