using namespace std;

int n,t=1, neo;
LL k;

string solve(){
  cin >> n >> k;
  string codes;
  cin >> codes;

  int j = 0;

  R(i, SZ(codes)){
    if(codes[i] == '0'){
      neo = min(1ll*(i - j), k);
      k -= neo;
      codes[i] = '1';
      codes[i-neo] = '0';
      j++;
    }
  }

  return codes;
}

int main(){

  cin >> t;
  while(t--) cout << solve() << endl;

}
