    if(!criba[i])
      for(LL j=i*i;j<N6;j+=i)
	criba[j]=true;

  LL p, primes=0;

  for(int i=2;i<N6;i++)
    if( !criba[i] and n%i == 0 ){primes++;p = i;}

  if(primes>1) return 1;
