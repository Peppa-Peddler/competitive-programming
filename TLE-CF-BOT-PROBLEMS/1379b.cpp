using namespace std;

int t = 1;

LL l, r, m;

void solve(){
  cin >> l >> r >> m;

  LL k = r - l;
  LL ml = max(m - k, 1LL);
  LL mr = m + k;
  LL ceil = 0;
  LL b,c;

  for(LL a=l; a <=r;a++){
    ceil = (ml/a + bool(ml%a!=0))*a;
    if( ceil <= mr ){
      k = ceil - m;
      if(k > 0){
	b = l;
	c = l + k;
      } else{
	b = r;
	c = r + k;
      }
      cout << a << " " << b << " " << c << endl;
      return;
    }
  }
}

int main(){

  cin >> t;
  //while(t--) cout << solve() << endl;
  while(t--) solve();

}
