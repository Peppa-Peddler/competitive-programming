using namespace std;

int n, k, t = 1;
long double m;

double a[ N5 ][ 2 ];

long double dist( long double time, bool dir ){
  int i = 0;
  long double d = 0;
  while( i < n ){
    if( time < (a[ i ][ dir ] - d)/( i + 1 ) ) break;
    time -= (a[ i ][ dir ] - d)/( i + 1 );
    d = a[ i ][ dir ];
    i++;
  }
  d += (i + 1)*time;
  return d;
}

long double solve(){
  cin >> n >> m;
  R(i, n){
    cin >> a[ i ][ 0 ];
    a[ n - i - 1 ][ 1 ]  = m - a[ i ][ 0 ];
  }
  long double l = 0, r = m + 1, mid, dl, dr;
  while(  r - l > 0.0000000001 ){
    mid = (r + l) / 2;
    dl = dist( mid, 0 );
    dr = m - dist( mid, 1 );
    if( dr - dl >= 0.0000000001 ) l = mid;
    else r = mid;
  }
  return l;
}

