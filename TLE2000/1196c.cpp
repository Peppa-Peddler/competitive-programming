using namespace std;

int n, m, k, t = 1;

PII x[ N5 ];
PII y[ N5 ];
bool f[ N5 ][ 4 ];

int getx(){
  int i = 0, id, k, ans;
  while( i < n ){
    id = x[i].S;
    if( !f[id][2] ) break;
    i++;
  }
  if( i == n ) return i - 1;
  ans = i;
  i ++;
  while( i < n && x[i].F == x[i - 1].F ) i++;
  while( i < n ){
    id = x[i].S;
    if( !f[id][0] ) break;
    i++;
  }
  if( i >= n ){
    return ans;
  }
  return -1;
}

int gety(){
  int i = 0, id, k, ans;
  while( i < n ){
    id = y[i].S;
    if( !f[id][1] ) break;
    i++;
  }
  if( i == n ) return i - 1;
  ans = i;
  i ++;
  while( i < n && y[i].F == y[i - 1].F ) i++;
  while( i < n ){
    id = y[i].S;
    if( !f[id][3] ) break;
    i++;
  }
  if( i >= n ){
    return ans;
  }
  return -1;
}

int solve(){
  cin >> n;

  R(i, n){
    cin >> x[i].F >> y[i].F >> f[i][0] >> f[i][1] >> f[i][2] >> f[i][3];
    x[i].S = i;
    y[i].S = i;
  }

  sort(x, x + n);
  sort(y, y + n);

  int ansx = getx();
  int ansy = gety();

  if( ansx == -1 or ansy == -1 ){
    cout << "0" << endl;
    return 0;
  }

  cout << "1 " << x[ansx].F << " " << y[ansy].F << endl;

  return 0;
}

int main(){

  cin >> t;
  //while(t--) cout << solve() << endl;
  while(t--) solve();

}
