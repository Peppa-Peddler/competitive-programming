using namespace std;

int n, m, k, t = 1;

string a,b;
int s[ 2*N5 ];

LL q(int l, int r){
  int sl, sr = s[ r ];
  if( l < 0 ) sl = 0;
  else sl = s[ l - 1 ];
  return sr - sl;
}

LL solve(){

  cin >> a >> b;
  int prev = 0;
  R(i, len(b)){
    s[ i ] = prev;
    s[ i ] += b[i] == '1';
    prev = s[ i ];
  }

  LL ans = 0;

  R(i, len(a)){
    int l = i, r = len(b) - len(a) + i;
    if( a[ i ] == '0'){
      ans += q(l, r);
    } else {
      ans += r - l + 1 - q(l, r);
    }

  }

  return ans;
}

