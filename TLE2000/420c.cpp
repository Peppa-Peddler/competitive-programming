using namespace std;

int n, p, k, t = 1;

int N[ 3*N5 ];
int s[ 3*N5 ]={};

PII ps[ 3*N5 ];

LL solve(){
  cin >> n >> p;

  int a, b;

  R(i, n){
    scanf("%d %d", &a, &b);
    a--, b--;
    ps[ i ].F = min(a, b);
    ps[ i ].S = max(a, b);
    N[ a ]++;
    N[ b ]++;
  }

  R(i, n){
    s[ N[ i ] ]++;
  }

  int prev = 0;
  R(i, n){
    prev += s[ n - i - 1 ];
    s[ n - i - 1 ] = prev;
  }

  LL ans = 0;

  R(i, n){
    if( p <= N[ i ] ) {ans += n - 1; continue;}
    ans += s[ p - N[ i ] ];
    if( N[ i ] >= p - N[ i ] ) ans --;
  }

  ans /= 2;

  sort( ps, ps + n );
  int c = 1, i = 1, sum;

  while( i < n ){

    if( ps[ i ] == ps[ i - 1 ] ){
      c++;
    } else {
      a = ps[ i - 1 ].F;
      b = ps[ i - 1 ].S;
      sum = N[ a ] + N[ b ];
      if( sum >= p and sum - c < p ) ans--;
      c = 1;
    }
    i++;
  }

  a = ps[ i - 1 ].F;
  b = ps[ i - 1 ].S;
  sum = N[ a ] + N[ b ];
  if( sum >= p and sum - c < p ) ans--;

  return ans;
}

