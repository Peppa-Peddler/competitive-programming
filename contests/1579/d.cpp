using namespace std;

int n, m, k, t = 1;

vector<PII> ans;

LL solve(){
    cin >> n;
	ans.clear();
	
	PQ< PII > Q;
	
	int val;
	
	R(i, n){
		cin >> val;
		if(val)
		Q.push( MP(val, i +1) );
	}

	PII a ,b;

	while(!Q.empty()){
		a = Q.top();
		Q.pop();
		if(Q.empty()) break;
		b = Q.top();
		Q.pop();
		a.F--, b.F--;
		if(a.F) Q.push(a);
		if(b.F) Q.push(b);
		ans.PB(MP(a.S, b.S));
	}

	cout << len(ans) << endl;
	R(i, len(ans)){
		cout << ans[ i ].F << " " << ans[ i ].S << endl;
	}
	
    return 0;
}

int main(){

    cin >> t;
    //while(t--) cout << solve() << endl;
    while(t--) solve();

}
